
 // Simple Calculator in C++
 

#include <iostream>
using namespace std;

int main() {
    // Declare variables
    int a = 10, b = 5;

    // Perform calculations and display output
    cout << "Addition = " << a + b << endl;
    cout << "Subtraction = " << a - b << endl;
    cout << "Multiplication = " << a * b << endl;
    cout << "Division = " << a / b << endl;

    return 0;
}

